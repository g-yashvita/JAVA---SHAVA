import java.util.Scanner;

class SimpleInterestCalculator
{
    void a()
    
    {
        Scanner jj=new Scanner(System.in);
        
        System.out.println("----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------");
        System.out.println("-----------------------------------------------------------SIMPLE INTEREST CALCULATOR --------------------------------------------------------------------------------------------------");
        System.out.println("----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------");
        
        
        System.out.println("ENTER THE PRINCIPAL");
        int P=jj.nextInt();
        
        System.out.println("ENTER THE RATE");
        int R=jj.nextInt();
        
        System.out.println("ENTER THE TIME");
        int T=jj.nextInt();
        
        int simp=(P*T*R)/100;
        System.out.println(simp + " : IS THE SIMPLE INTEREST FOR THE GIVEN PRINCIPAL , RATE AND TIME <3 ");
        
        System.out.println("----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------");
        System.out.println("-----------------------------------------------------------always and anytime ! --------------------------------------------------------------------------------------------------------");
        System.out.println("----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------");
        
    }
}
